package com.lc.sofa.core.framework.web.util;

import com.lc.sofa.core.framework.basis.exception.SofaException;
import com.lc.sofa.core.framework.constants.ExceptionConstants;

/**
 * AJAX请求异常类
 * 
 * @author Administrator
 * @version 1.0, 2013-11-6
 * @since 1.0, 2013-11-6
 */
public class AjaxException extends SofaException {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1117524010316720868L;

	public String getExceptionCode() {

		return ExceptionConstants.SOFA_AJAX_OPERATION_EXCEPTION;
	}

	public AjaxException() {

		super();

	}

	public AjaxException(String message) {

		super(message);
	}

	public AjaxException(String message, Throwable cause) {

		super(message, cause);
	}

	public AjaxException(Throwable cause) {

		super(cause);
	}

}
