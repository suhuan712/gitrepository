package com.lc.sofa.core.framework.basis.vo;

public abstract class BasisVO implements ValueObject {

	/**
	 * 
	 */
	private static final long serialVersionUID = 2643988013245284932L;

	/**
	 * 记录的ID
	 */
	private String id;

	/**
	 * 记录的逻辑删除标识
	 */
	private Integer deltag;

	/**
	 * 时间戳
	 */
	private String timetag;

	/**
	 * @return the id
	 */
	public String getId() {

		return id;
	}

	/**
	 * @param id
	 *            the id to set
	 */
	public void setId(String id) {

		this.id = id;
	}

	/**
	 * @return the deltag
	 */
	public Integer getDeltag() {

		return deltag;
	}

	/**
	 * @param deltag
	 *            the deltag to set
	 */
	public void setDeltag(Integer deltag) {

		this.deltag = deltag;
	}

	/**
	 * @return the timetag
	 */
	public String getTimetag() {

		return timetag;
	}

	/**
	 * @param timetag
	 *            the timetag to set
	 */
	public void setTimetag(String timetag) {

		this.timetag = timetag;
	}

}
