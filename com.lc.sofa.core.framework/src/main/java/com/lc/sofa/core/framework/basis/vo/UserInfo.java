package com.lc.sofa.core.framework.basis.vo;

/**
 * 
 * 
 * 用户信息实体类
 * @author      YZ
 * @version 1.0, 2013-10-30
 * @since 1.0, 2013-10-30
 */
public class UserInfo extends  BasisVO{

	/**
	 * 
	 */
	private static final long serialVersionUID = -6915467820700335908L;
	
	private String id;
	
    private String loginCode;
    
    private String loginName;

    private String errorCode;
    
    private String password;
    
    private String loginIp;

	private String freezeFlag;
    
    private String freezeTime;
    
    private String firWroPassTime;
    
    private String logouttime;
    
    private String logintime;
    
    private int wroPassTimes;

	
    public UserInfo(){
    	
    	
    }
    
    public UserInfo(String loginId,String loginName,String loginCode){
    	   this.id=loginId;
    	   this.loginCode=loginCode;  
    	   this.loginName=loginName;
    }
    
    /**
	 * @return the loginCode
	 */
	public String getLoginCode() {
	
		return loginCode;
	}

	
	/**
	 * @param loginCode the loginCode to set
	 */
	public void setLoginCode(String loginCode) {
	
		this.loginCode = loginCode;
	}

	
	/**
	 * @return the loginName
	 */
	public String getLoginName() {
	
		return loginName;
	}

	
	/**
	 * @param loginName the loginName to set
	 */
	public void setLoginName(String loginName) {
	
		this.loginName = loginName;
	}

	
	

	
	/**
	 * @return the id
	 */
	public String getId() {
	
		return id;
	}


	
	/**
	 * @param id the id to set
	 */
	public void setId(String id) {
	
		this.id = id;
	}
    
	/**
	 * @return the errorCode
	 */
	public String getErrorCode() {
	
		return errorCode;
	}

	
	/**
	 * @param errorCode the errorCode to set
	 */
	public void setErrorCode(String errorCode) {
	
		this.errorCode = errorCode;
	}

	 public String toString(){
    	   return "id="+this.id+",loginCode="+this.loginCode+",loginName="+this.loginName;
    	
    }

	
	/**
	 * @return the password
	 */
	public String getPassword() {
	
		return password;
	}

	
	/**
	 * @param password the password to set
	 */
	public void setPassword(String password) {
	
		this.password = password;
	}

	
	/**
	 * @return the serialversionuid
	 */
	public static long getSerialversionuid() {
	
		return serialVersionUID;
	}

	
	/**
	 * @return the freezeFlag
	 */
	public String getFreezeFlag() {
	
		return freezeFlag;
	}

	
	/**
	 * @param freezeFlag the freezeFlag to set
	 */
	public void setFreezeFlag(String freezeFlag) {
	
		this.freezeFlag = freezeFlag;
	}

	
	/**
	 * @return the freezeTime
	 */
	public String getFreezeTime() {
	
		return freezeTime;
	}

	
	/**
	 * @param freezeTime the freezeTime to set
	 */
	public void setFreezeTime(String freezeTime) {
	
		this.freezeTime = freezeTime;
	}

	
	/**
	 * @return the firWroPassTime
	 */
	public String getFirWroPassTime() {
	
		return firWroPassTime;
	}

	
	/**
	 * @param firWroPassTime the firWroPassTime to set
	 */
	public void setFirWroPassTime(String firWroPassTime) {
	
		this.firWroPassTime = firWroPassTime;
	}

	
	/**
	 * @return the wroPassTimes
	 */
	public int getWroPassTimes() {
	
		return wroPassTimes;
	}

	
	/**
	 * @param wroPassTimes the wroPassTimes to set
	 */
	public void setWroPassTimes(int wroPassTimes) {
	
		this.wroPassTimes = wroPassTimes;
	}

	/**
	 * @return the loginIp
	 */
	public String getLoginIp() {
	
		return loginIp;
	}

	
	/**
	 * @param loginIp the loginIp to set
	 */
	public void setLoginIp(String loginIp) {
	
		this.loginIp = loginIp;
	}

	
	/**
	 * @return the logouttime
	 */
	public String getLogouttime() {
	
		return logouttime;
	}

	
	/**
	 * @param logouttime the logouttime to set
	 */
	public void setLogouttime(String logouttime) {
	
		this.logouttime = logouttime;
	}

	
	/**
	 * @return the logintime
	 */
	public String getLogintime() {
	
		return logintime;
	}

	
	/**
	 * @param logintime the logintime to set
	 */
	public void setLogintime(String logintime) {
	
		this.logintime = logintime;
	}

	
}
