/**
 * 
 * WEB组件URL转换服务异常
 * @author   YZ
 * @version 1.0, 2013-10-25
 * @since 1.0, 2013-10-25
 */
public class ComponentUrlException extends SofaException{

    /**
	 * 
	 */
	private static final long serialVersionUID = -5432425581644045752L;

	public String getExceptionCode() {

		   return ExceptionConstants.SOFA_COMPONENT_URL_EXCEPTION;
	}
	
    public ComponentUrlException(String message){
    	   super(message);
    	
    }
    
     public ComponentUrlException(String message,Throwable cause){
    	  super(message,cause);
    	
    }
     
     public ComponentUrlException(Throwable cause){
    	 super(cause);
     	
     }
     
    
    
	
}
