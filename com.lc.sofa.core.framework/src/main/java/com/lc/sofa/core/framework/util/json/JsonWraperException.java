package com.lc.sofa.core.framework.util.json;

import com.lc.sofa.core.framework.basis.exception.SofaException;
import com.lc.sofa.core.framework.constants.ExceptionConstants;

/**
 * 
 * JSON包装异常
 * @author   YZ
 * @version 1.0, 2013-10-24
 * @since 1.0, 2013-10-24
 */
public class JsonWraperException extends SofaException{

	/**
	 * 
	 */
	private static final long serialVersionUID = -5297349084337434701L;

	public String getExceptionCode() {

		return ExceptionConstants.SOFA_JSON_WRAPPER_EXCEPTION_CODE;
	}
	
	public JsonWraperException(String message){
		   super(message);
	}
	
	public JsonWraperException(String message,Throwable cause){
		    super(message,cause);
		 
	}
	
	public JsonWraperException(Throwable cause){
		    super(cause);
	}

}
